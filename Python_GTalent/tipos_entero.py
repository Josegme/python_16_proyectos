# Numeros enteros en Python

numEntero = 7

print(numEntero, type(numEntero))

print(isinstance(numEntero, int)) # numEntero es de tipo entero?

numEntero = 7854696424
print(numEntero, type(numEntero))

# otras representaciones de enteros puede estar en Binario

numBinario = 0b110
numHexa = 0xFA
numOctal = 0o12

print(numBinario, type(numBinario))
print(numHexa, type(numHexa))
print(numOctal, type(numOctal))