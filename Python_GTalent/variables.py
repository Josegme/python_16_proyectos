"""
en Python no hace falta declarar las variables,
una vez que asigno un valor a una variable el tipo se crea
automaticamente de manera interna.
"""

# Variables y asignacion

edad = 18 # variable int
print(edad)

nombre = 'Alejandro' # variable String
print(nombre)

valorPi=3.1416 # variable double
print(valorPi)

# se puede asignar de manera multiple

edad1, nombre2, valorDouble = 28, 'Ale', 3.2658

print(edad1)
print(nombre2)
print(valorDouble)